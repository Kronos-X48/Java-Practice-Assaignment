/*
Write a java program to print an equilateral triangle using star ' * '.
 */
public class Q4_Assaignment_1 {
    public static void main(String[] args){
        System.out.println("           *            ");
        System.out.println("          * *            ");
        System.out.println("         * * *            ");
        System.out.println("        * * * *            ");
        System.out.println("       * * * * *            ");
    }
}
